def load_from_f(file_path: str):
    data = list()
    with open(file_path, "r") as f:
        for line in f:
            data.append(line.rstrip())
    return sorted(data)


class LAHeart(Dataset):
    """ LA Dataset """

    def __init__(self, train_path, load_path, num_classes=2,
                 num=None, transform=None, is_3d=True):
        self.transform = transform
        self.load_path = load_path

        self.image_list = load_from_f(train_path)

        if num is not None:
            self.image_list = self.image_list[:num]
        self.num_classes = num_classes
        self.is_3d = is_3d
        print("total {} samples".format(len(self.image_list)))

    def __len__(self):
        return len(self.image_list)

    def __getitem__(self, idx):
        image_name = self.image_list[idx]
        h5f = h5py.File(os.path.join(self.load_path, image_name), "r")
        image = h5f['image'][:]
        label = h5f['label'][:]
        sample = {'image': image, 'label': label.astype(np.float32)}
        if self.transform:
            sample = self.transform(sample)
        sample["image"] = sample["image"]
        return sample


class LAHeart_OneHot(Dataset):
    """ LA Dataset """

    def __init__(self, train_path, load_path, num_classes=2,
                 num=None, transform=None, is_3d=True):
        self.transform = transform
        self.load_path = load_path
        self.num_classes = num_classes
        self.image_list = load_from_f(train_path)

        if num is not None:
            self.image_list = self.image_list[:num]
        self.is_3d = is_3d
        print("total {} samples".format(len(self.image_list)))

    def __len__(self):
        return len(self.image_list)

    def __getitem__(self, idx):
        permute_label_dim = (3, 2, 0, 1) if self.is_3d else (2, 0, 1)
        image_name = self.image_list[idx]
        h5f = h5py.File(os.path.join(self.load_path, image_name), "r")
        image = h5f['image'][:]
        label = h5f['label'][:]
        sample = {'image': image, 'label': label.astype(np.float32)}
        if self.transform:
            sample = self.transform(sample)
        sample["image"] = sample["image"]
        if self.is_3d:
            sample["image"] = sample["image"].permute(0, 3, 1, 2)
        temp_label = F.one_hot(torch.squeeze(sample["label"].to(torch.int64), dim=0),
                               num_classes=self.num_classes)
        sample["label"] = temp_label.permute(permute_label_dim).to(torch.float32)
        return sample


class CenterCrop(object):
    def __init__(self, output_size):
        self.output_size = output_size

    def __call__(self, sample):
        image, label = sample['image'], sample['label']

        # pad the sample if necessary
        if label.shape[0] <= self.output_size[0] or label.shape[1] <= self.output_size[1]:
            pw = max((self.output_size[0] - label.shape[0]) // 2 + 3, 0)
            ph = max((self.output_size[1] - label.shape[1]) // 2 + 3, 0)
            image = np.pad(image, [(pw, pw), (ph, ph)], mode='constant', constant_values=0)
            label = np.pad(label, [(pw, pw), (ph, ph)], mode='constant', constant_values=0)

        (w, h) = image.shape

        w1 = int(round((w - self.output_size[0]) / 2.))
        h1 = int(round((h - self.output_size[1]) / 2.))

        label = label[w1:w1 + self.output_size[0], h1:h1 + self.output_size[1]]
        image = image[w1:w1 + self.output_size[0], h1:h1 + self.output_size[1]]

        return {'image': image, 'label': label}


